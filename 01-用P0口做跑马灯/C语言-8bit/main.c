/*************  功能说明    **************

本例程基于AI8051U为主控芯片的实验箱进行编写测试.

程序使用P0口来演示跑马灯，输出低驱动。

下载时, 选择时钟 24MHZ (用户可自行修改频率).

******************************************/

#include "AI8051U.h"
#include "stdio.h"
#include "intrins.h"

typedef 	unsigned char	u8;
typedef 	unsigned int	u16;
typedef 	unsigned long	u32;

#define MAIN_Fosc        24000000UL

//==========================================================================


/*************  本地常量声明    **************/


/*************  IO口定义    **************/

/*************  本地变量声明    **************/


/*************  本地函数声明    **************/

void  delay_ms(u8 ms);

/****************  外部函数声明和外部变量声明 *****************/


/******************** 主函数 **************************/
void main(void)
{
    P_SW2 |= 0x80; //扩展寄存器(XFR)访问使能

    P0M1 = 0x00;   P0M0 = 0xff;   //设置为推挽输出
    P1M1 = 0x00;   P1M0 = 0x00;   //设置为准双向口
    P2M1 = 0x00;   P2M0 = 0x00;   //设置为准双向口
    P3M1 = 0x00;   P3M0 = 0x00;   //设置为准双向口
    P4M1 = 0x00;   P4M0 = 0x00;   //设置为准双向口
    P5M1 = 0x00;   P5M0 = 0x00;   //设置为准双向口
    P6M1 = 0x00;   P6M0 = 0x00;   //设置为准双向口
    P7M1 = 0x00;   P7M0 = 0x00;   //设置为准双向口

    P40 = 0;		//LED Power On

    while(1)
    {
        P00 = 0;		//LED On
        delay_ms(250);
        P00 = 1;		//LED Off

        P01 = 0;		//LED On
        delay_ms(250);
        P01 = 1;		//LED Off

        P02 = 0;		//LED On
        delay_ms(250);
        P02 = 1;		//LED Off

        P03 = 0;		//LED On
        delay_ms(250);
        P03 = 1;		//LED Off

        P04 = 0;		//LED On
        delay_ms(250);
        P04 = 1;		//LED Off

        P05 = 0;		//LED On
        delay_ms(250);
        P05 = 1;		//LED Off

        P06 = 0;		//LED On
        delay_ms(250);
        P06 = 1;		//LED Off

        P07 = 0;		//LED On
        delay_ms(250);
        P07 = 1;		//LED Off
    }
}

//========================================================================
// 函数: void  delay_ms(unsigned char ms)
// 描述: 延时函数。
// 参数: ms,要延时的ms数, 这里只支持1~255ms. 自动适应主时钟.
// 返回: none.
// 版本: VER1.0
// 日期: 2013-4-1
// 备注: 
//========================================================================
void  delay_ms(u8 ms)
{
     u16 i;
     do{
          i = MAIN_Fosc / 10000;
          while(--i);
     }while(--ms);
}
